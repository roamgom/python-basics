#제곱을 구하기 위한 연산 **
#x ** y 와같이 사용한 경우 x의y 제곱을 계산한다

a = 3
b = 4
a ** b

# 나머지를 구하기 위한 연산 %
# x % y 와 같이 사용한 경우 x 를 y로 나눈 나머지를 계산한다

a = 4
b = 3
a % b

# 나머지를 고려하지 않고 몫만을 구하는 연산 //
# x // y 와 같이 사용한 경우 x를y로 나눈 몫을 계산

a = 7
b = 4
a//b

a = -7
b = 4

